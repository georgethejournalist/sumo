# import common functions for netedit tests
import os
import sys

testRoot = os.path.join(os.environ.get('SUMO_HOME', '.'), 'tests')
neteditTestRoot = os.path.join(
    os.environ.get('TEXTTEST_HOME', testRoot), 'netedit')
sys.path.append(neteditTestRoot)
import neteditTestFunctions as netedit  # noqa

# Open netedit
neteditProcess, referencePosition = netedit.setupAndStart(neteditTestRoot)

# go to additional mode
netedit.additionalMode()

# select E2
netedit.changeElement("e2Detector")

# set invalid vehicle types (invalid IDs)
netedit.changeDefaultValue(7, "%%;$$$ %%$$ type.3")

# try to create E2 with invalid vehicle types
netedit.leftClick(referencePosition, 300, 220)

# set valid vehicle types
netedit.changeDefaultValue(7, "type1 type2 type3")

# create E2 with valid vehicle types
netedit.leftClick(referencePosition, 300, 220)

# Check undo redo
netedit.undo(referencePosition, 1)
netedit.redo(referencePosition, 1)

# save additionals
netedit.saveAdditionals(referencePosition)

# save network
netedit.saveNetwork(referencePosition)

# quit netedit
netedit.quit(neteditProcess)
